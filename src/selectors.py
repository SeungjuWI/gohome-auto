"""DOM 셀렉터 모음.

사이트 UI가 바뀌면 이 파일만 고치면 되도록 셀렉터를 한 곳에 모은다.

아래 값은 실제 로그인 페이지(https://gw.likelion.net/#/login) DOM을 직접 확인해 확정한 것.
(OBTButton 계열 SPA — 같은 텍스트의 숨은 버튼이 DOM에 다수 존재하므로
 버튼은 반드시 ':visible' + 정확 텍스트(:text-is)로 집는다.)

⚠️ 단, 출근/퇴근 버튼(CHECKIN/CHECKOUT)과 로그인 성공 마커는 아직 실제 확인 전 추정값.
"""

# ─────────────────────────────────────────────────────────────
# 1단계 화면: 회사코드(disabled) + 아이디 → '다음'
# ─────────────────────────────────────────────────────────────

# 회사코드 입력란 — 'likelion'이 채워진 disabled 박스. 타이핑 금지, 값 검증용.
COMPANY_CODE_INPUT = "css=#reqCompCd"

# 아이디 입력란
USER_ID_INPUT = "css=#reqLoginId"

# '다음' 버튼 — 숨은 동일 텍스트 버튼이 여러 개라 visible + 정확일치(따옴표)로 한정
NEXT_BUTTON = 'button:visible >> text="다음"'

# ─────────────────────────────────────────────────────────────
# 2단계 화면: 비밀번호 → '로그인'
# ─────────────────────────────────────────────────────────────

# 비밀번호 입력란 — DOM엔 처음부터 있으나 '다음' 클릭 후에야 visible 이 된다.
# 2단계 진입 완료 판정의 기준 요소이기도 하다.
PASSWORD_INPUT = "css=#reqLoginPw"

# '로그인' 버튼 — visible + 정확일치(따옴표) ('로그인 화면으로' 등과 구분)
LOGIN_BUTTON = 'button:visible >> text="로그인"'

# ─────────────────────────────────────────────────────────────
# 출근 / 퇴근 버튼 (⚠️ 실제 DOM 확인 후 확정 필요 — 현재는 추정 텍스트)
# ─────────────────────────────────────────────────────────────
CHECKIN_BUTTON = 'text=출근'
CHECKOUT_BUTTON = 'text=퇴근'

# 출퇴근 처리 완료 판정 요소 (토스트/상태 텍스트 등, 실제 확인 필요)
ACTION_RESULT_MARKER = 'css=.toast, .alert, [class*="complete"]'
